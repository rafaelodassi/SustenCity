var _imgLoaded;
var canvas = gv('#canvas');
var stats = new Stats();

function init () {
    canvas.createStage({
        screen: 'fullscreen'
    });

    canvas.preloadImages(objImages, {
        progress: function (percent) {
            $("#loading .loading").html("Carregando - " + percent + "%");
        },

        completed: function (imgLoaded) {
            _imgLoaded = imgLoaded;

            $("#loading").animate({opacity: 0}, 500, function () {
                $(this).remove();
            });

            $("#intro button").click( function (){
                $("#intro").fadeTo( 300, 0, function() {
                    $(this).hide();
                });
            });

            /*add imagem de fundo (mapa)*/
            var bg = canvas.addChild('bg', imgLoaded.bg);
            bg.update({ x: (-bg.width/2) + (canvas.width/2), y: (-bg.height/2) + (canvas.height/2) });

            /*add tileMap de construcao (area de construcao)*/
            canvas.addChild('tileMapRows', tileMapRows).update({ alpha: 0 });

            /*add tileMap de construcao (objetos)*/
            canvas.addChild('tileMap', tileMap);

            /*add imagem nuvem 1*/
            var cloud1 = canvas.addChild('cloud1', imgLoaded.cloud1);
            cloud1.update({
                x: (canvas.width/2) - (bg.width/2) - (cloud1.width),
                y: (canvas.height/2) - (cloud1.height/2) - 200,
                alpha: .7
            });

            /*add imagem nuvem 2*/
            var cloud2 = canvas.addChild('cloud2', imgLoaded.cloud2);
            cloud2.update({
                x: (canvas.width/2) - (bg.width/2) - (cloud2.width),
                y: (canvas.height/2) - (cloud2.height/2) + 300,
                alpha: .7
            });

            /*add imagem birds*/
            var birds = canvas.addChild('birds', imgLoaded.birds);
            birds.update({
                x: (canvas.width/2) - (bg.width/2) - (birds.width) - 300,
                y: (canvas.height/2) - (birds.height/2)
            });

            state.initialize();
            canvas.scroll(true, bg);
            build.initialize();
            gavia.gameLoop(canvas);            

            gavia.addLoop("animateCloud1", {
                target: cloud1,
                duration: 360,
                loop: true,
                properties: {
                    x: (canvas.width/2) + (bg.width/2)
                }
            });

            gavia.addLoop("animateCloud2", {
                target: cloud2,
                duration: 300,
                loop: true,
                properties: {
                    x: (canvas.width/2) + (bg.width/2)
                }
            });

            gavia.addLoop("animateBirds", {
                target: birds,
                duration: 50,
                loop: true,
                properties: {
                    x: (canvas.width/2) + (bg.width/2)
                }
            });

            gavia.addLoop("updateReward", {
                update: function () {
                    for ( var key in canvas.childrens ) {
                        if (key.search("trash") != -1 || key.search("lotus") != -1) {
                            var rewardBubble = canvas.childrens[key];

                            if ( (gavia.loopTimestamp >= rewardBubble.extra.startTime + rewardBubble.extra.rewardTime) && rewardBubble.alpha == 0 ) {
                                rewardBubble.update({ alpha: 1, scaleX: 1, scaleY: 1 });

                                rewardBubble.addMouseDown(function (target, event) {
                                    var type = target.name.split("_")[2];

                                    if ( type == "trash" )
                                        state.addDrops(target.extra.rewardValue);
                                    else
                                        state.addLotus(target.extra.rewardValue);

                                    gavia.addLoop(target.name + "_animate", {
                                        target: target,
                                        duration: .1,
                                        loop: false,
                                        properties: {
                                            scaleX: 1.2,
                                            scaleY: 1.2,
                                            alpha: 0
                                        }
                                    });

                                    var rewardItem = canvas.addChild(type, imgLoaded[type + "Reward"]),
                                        rewardItemY = (type == "trash") ? 30 : 100;

                                    rewardItem.x = event.clientX - 20;
                                    rewardItem.y = event.clientY - 20;                                    

                                    gavia.addLoop("animateReward", {
                                        target: rewardItem,
                                        duration: .5,
                                        loop: false,
                                        contextScroll: false,
                                        properties: {
                                            x: (canvas.width - 80),
                                            y: rewardItemY,
                                            alpha: 0
                                        },
                                        onComplete: function () {
                                            canvas.removeChild(type);
                                        }
                                    });

                                    target.update({ extra: { rewardValue: target.extra.rewardValue, rewardTime: target.extra.rewardTime, startTime: gavia.loopTimestamp } });
                                });                             
                            }
                        }
                    }                        
                },
                duration: -1
            });
        }
    });
}

init();